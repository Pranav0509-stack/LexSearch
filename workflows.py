def run_generic(key: str, text: str) -> dict[str, Any]:
    wf = GENERIC_WORKFLOWS.get(key)
    if not wf:
        raise ValueError(f"unknown workflow: {key}")
    if not router.available_providers():
        return {"output_markdown": "(no LLM provider configured)", "refused": True, "key": key, "title": wf["title"]}
    user = f"Input text (truncate if >10k chars):\n{text[:10000]}\n\nProduce the output per your system instructions."
    resp = router.generate(wf["system"], user, temperature=0.15, max_tokens=1800)
    return {
        "output_markdown": resp.text,
        "key": key,
        "title": wf["title"],
        "refused": False,
        "llm": resp.to_dict(),
    }


def list_generic_workflows() -> list[dict]:
    return [{"key": k, "title": v["title"], "sub": v["sub"]} for k, v in GENERIC_WORKFLOWS.items()]
