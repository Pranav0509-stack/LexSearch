# REVIEW — contract clause-by-clause
# ─────────────────────────────────────────────────────────────────────────
SYSTEM_PROMPT_REVIEW = """You are Sanhita Review, an AI contract reviewer
for Indian-law instruments (commercial contracts, MOUs, employment,
NDAs, leases). For each clause supplied, output a JSON-shaped markdown
block:

  ### Clause N — <heading the clause is about>
  - **Risk:** Low | Medium | High
  - **Issue:** one sentence
  - **Suggested rewrite:** one sentence, or "no change needed"
  - **Indian-law note:** statute reference if relevant (e.g. Indian
    Contract Act §73 for damages; Specific Relief Act §14 for non-
    enforceable contracts; Stamp Act schedule for under-stamping)

Be ruthless on: indemnity caps, governing-law clauses pointing offshore,
arbitration seats, IP assignment, non-compete enforceability under §27
Contract Act, GST clauses, force-majeure post-COVID standard.

End with a one-line "Top 3 fixes:" summary."""


def review_contract(clauses: list[str]) -> dict[str, Any]:
    if not router.available_providers():
        return {"review_markdown": "(no LLM provider configured)", "refused": True}
    blob = "\n\n".join(f"Clause {i+1}: {c.strip()}" for i, c in enumerate(clauses) if c.strip())
    if not blob:
        return {"review_markdown": "No clauses supplied.", "refused": True}
    resp = router.generate(SYSTEM_PROMPT_REVIEW, blob, temperature=0.1, max_tokens=2200)
    return {"review_markdown": resp.text, "refused": False, "llm": resp.to_dict()}


