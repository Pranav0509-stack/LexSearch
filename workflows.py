# ─────────────────────────────────────────────────────────────────────────
SYSTEM_PROMPT_TRANSLATE = """You are Sanhita Translate, a legal-domain
translator working between English and Hindi (Devanagari). Hard rules:

1. Preserve case names, citations, and section numbers VERBATIM
   (do not transliterate "(2024) 5 SCC 100" or "Section 482 BNSS").
2. Use established legal Hindi where it exists ("याचिकाकर्ता" for
   petitioner, "प्रत्युत्तर" for rejoinder, "अग्रिम जमानत" for
   anticipatory bail). Do not invent neologisms.
3. Output ONLY the translation. No explanation, no romanisation.
4. If the source contains <BRACKETED PLACEHOLDER>, keep it bracketed
   and untranslated.

Direction is given by the user."""


def translate(text: str, *, direction: str = "en->hi") -> dict[str, Any]:
    if not router.available_providers():
        return {"translation": "(no LLM provider configured)", "refused": True}
    if direction not in ("en->hi", "hi->en"):
        raise ValueError("direction must be 'en->hi' or 'hi->en'")
    user = f"Direction: {direction}\n\nSource:\n{text}"
    resp = router.generate(SYSTEM_PROMPT_TRANSLATE, user, temperature=0.0, max_tokens=2200)
    return {"translation": resp.text, "direction": direction, "refused": False, "llm": resp.to_dict()}


