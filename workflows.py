def citator_summary(case_title: str, excerpt: str, holdings: str = "") -> dict[str, Any]:
    if not router.available_providers():
        return {"summary_markdown": "(no LLM provider configured)", "refused": True}
    user = (
        f"Case title: {case_title}\n\n"
        f"Excerpt:\n{excerpt[:6000]}\n\n"
        + (f"Holdings (if separately extracted):\n{holdings[:2000]}\n\n" if holdings else "")
        + "Produce the citator summary as per your system instructions."
    )
    resp = router.generate(SYSTEM_PROMPT_CITATOR, user, temperature=0.0, max_tokens=1200)
    return {"summary_markdown": resp.text, "case_title": case_title, "refused": False, "llm": resp.to_dict()}


# ─────────────────────────────────────────────────────────────────────────
# REDLINE — clause-level redline with JSON suggestions
# ─────────────────────────────────────────────────────────────────────────
SYSTEM_PROMPT_REDLINE = """You are Sanhita Redline, an AI contract redliner
for Indian-law instruments. Given a contract or clauses, return a JSON
object with:

{
  "summary": ["<bullet point>", "<bullet point>", ...],
  "suggestions": [
    {
      "id": "<short slug>",
      "title": "<one-line title>",
      "type": "remove" | "replace" | "add",
      "original": "<exact text to remove or replace, verbatim; empty string for add>",
      "replacement": "<new text; empty string for remove>",
      "reason": "<one sentence Indian-law rationale, cite statute if relevant>"
    }
  ]
}

Rules:
1. Output ONLY valid JSON. No markdown fences, no preamble.
2. Cap at 15 suggestions. Prioritize high-risk clauses:
   indemnity caps, governing law pointing offshore, arbitration seat,
   non-compete under §27 Contract Act, IP assignment, stamp duty,
   force majeure, limitation of liability, GST gross-up.
3. "original" must be a verbatim substring of the supplied contract
   (trim whitespace to match). If you cannot quote verbatim, omit that
   suggestion — never paraphrase into "original".
4. "summary" is 3-5 bullets, each ≤12 words."""


def redline_contract(text: str) -> dict[str, Any]:
    if not router.available_providers():
        return {"summary": [], "suggestions": [], "refused": True, "reason": "no LLM"}
    import json as _json
    user = f"Contract text:\n{text[:12000]}\n\nProduce the redline JSON."
    resp = router.generate(SYSTEM_PROMPT_REDLINE, user, temperature=0.1, max_tokens=2200)
    raw = resp.text.strip()
    # Strip possible code fences the model might emit despite instructions
    if raw.startswith("```"):
        raw = raw.split("```", 2)[1]
        if raw.startswith("json"): raw = raw[4:]
        raw = raw.rsplit("```", 1)[0].strip()
    try:
        data = _json.loads(raw)
    except Exception:
        data = {"summary": ["(parse error — raw output returned)"], "suggestions": [], "raw": raw[:2000]}
    data["refused"] = False
    data["llm"] = resp.to_dict()
    return data


