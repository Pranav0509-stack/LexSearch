def generate_draft(template_key: str, facts: dict, db_template: dict | None = None) -> dict[str, Any]:
    """
    Generate a draft from either hardcoded or DB-backed template.

    facts: free-form dict of user-supplied params (parties, dates, amounts,
    fact pattern, court, etc). We pass them as JSON-ish text to the model;
    no schema enforcement beyond non-empty.

    db_template: if provided, uses the database template body + citations
    instead of the hardcoded DRAFT_TEMPLATES.
    """
    # Try DB template first, then fall back to hardcoded
    if db_template:
        title = db_template.get("title", template_key)
        body = db_template.get("body", "")
        citations = db_template.get("sample_citations", "")
        system = (
            f"You are Sanhita Draft, generating an Indian legal document.\n\n"
            f"TEMPLATE: {title}\n\n"
            f"Use the following template structure as your guide:\n{body[:3000]}\n\n"
            f"Hard rules:\n"
            f"1. Indian court convention and formatting.\n"
            f"2. Replace ALL bracketed placeholders with the user's facts.\n"
            f"3. Where a fact is missing, keep the <BRACKETED PLACEHOLDER>.\n"
            f"4. Do NOT fabricate case citations.\n"
        )
        if citations:
            system += f"\nReal citations you may reference:\n{citations[:1000]}\n"
        system += (
            '\n5. End with: "This draft is a working template generated by Sanhita '
            'Draft and must be settled by counsel before filing."\n\n'
            "Output strictly markdown."
        )
    else:
        tmpl = DRAFT_TEMPLATES.get(template_key)
        if not tmpl:
            raise ValueError(f"unknown template: {template_key}")
        title = tmpl["title"]
        system = tmpl["system"]

    if not router.available_providers():
        # No LLM — return the raw template body if available
        raw_body = db_template.get("body", "(no LLM provider configured)") if db_template else "(no LLM provider configured)"
        return {"draft_markdown": raw_body, "refused": True,
                "llm": {"provider": "none"}, "template": template_key}

    facts_block = "\n".join(f"- {k}: {v}" for k, v in (facts or {}).items() if v)
    user = (
        f"Facts supplied by counsel:\n{facts_block or '(none — generate a generic skeleton)'}\n\n"
        f"Generate the {title} as per your system instructions. "
        f"Where a fact is missing, insert <BRACKETED PLACEHOLDER> so counsel "
        "can fill it in."
    )
    resp = router.generate(system, user, temperature=0.2, max_tokens=3000)
    return {
        "draft_markdown": resp.text,
        "template": template_key,
        "title": title,
        "refused": False,
        "llm": resp.to_dict(),
    }


